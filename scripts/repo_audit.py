async def main():
    print("=" * 70)
    print("  LeakWatch Repository Integrity Audit")
    print(f"  Executed: {datetime.now().isoformat()}")
    print("=" * 70)

    # Step 1
    step1_repository_audit()

    # Step 2
    df = step2_dataset_validation()
