# STEP 3 — ML Model Validation
# ═══════════════════════════════════════════════════════════════════
def step3_model_validation(df):
    print("\n" + "=" * 70)
    print("STEP 3 — ML Model Validation")
    print("=" * 70)

    model_path = Path("models/isolation_forest.pkl")
    if not model_path.exists():
        print(f"ERROR: Model file not found at {model_path}")
        report["model_validation"] = {"status": "FAIL", "reason": "file not found"}
        return False

    print(f"  Model file: {model_path} ({model_path.stat().st_size:,} bytes)")

    # Load via project's model loader
    print("\n--- Loading via app.ml.model_loader ---")
    try:
        from app.ml.model_loader import get_model
        model = get_model()
        print(f"  Model loaded: OK")
        print(f"  Model type:   {type(model).__name__}")
        print(f"  Inner model:  {type(model.model).__name__}")
    except Exception as e:
        print(f"  Model loading FAILED: {e}")
        report["model_validation"] = {"status": "FAIL", "reason": str(e)}
        return False

    # Score sample transactions via scoring_engine
    print("\n--- Scoring Sample Transactions ---")
    from app.ml.feature_engineering import extract_transaction_features

    dept_stats = df.groupby("DEPARTMENT NAME")["AMOUNT"].agg(["mean", "std"]).to_dict("index")
    sample = df.sample(n=min(5, len(df)), random_state=42)

    results = []
    for _, row in sample.iterrows():
        txn = SimpleNamespace(
            voucher_number=row["VOUCHER NUMBER"],
            amount=float(row["AMOUNT"]),
            check_date=row["CHECK DATE"],
            department=row["DEPARTMENT NAME"],
            description="",
            vendor_name=row["VENDOR NAME"],
        )
        d = dept_stats.get(row["DEPARTMENT NAME"], {"mean": 0, "std": 0})
        features = extract_transaction_features(
            txn,
            recent_transaction_count=0,
            department_avg_amount=float(d["mean"]),
            department_std_amount=float(d["std"]) if not pd.isna(d["std"]) else 0.0,
        )
        score = model.score(features)

        risk_factors = []
        if features["is_large_transaction"]:
            risk_factors.append("large_transaction")
        if features["is_procurement"]:
            risk_factors.append("procurement_department")
        if abs(features["amount_zscore"]) > 3:
            risk_factors.append("department_spending_outlier")

        results.append({
            "voucher_number": row["VOUCHER NUMBER"],
            "leak_probability": score,
            "risk_factors": risk_factors,
        })

    print(f"\n  {'Voucher Number':<22} {'Leak Prob':>10}  Risk Factors")
    print("  " + "-" * 65)
    for r in results:
        factors = ", ".join(r["risk_factors"]) if r["risk_factors"] else "none"
        print(f"  {r['voucher_number']:<22} {r['leak_probability']:>10.4f}  {factors}")

    scoring_ok = all(0.0 <= r["leak_probability"] <= 1.0 for r in results)
    print(f"\n  Model scoring: {'OK' if scoring_ok else 'FAIL'}")
    print(f"  All scores in [0, 1]: {'YES' if scoring_ok else 'NO'}")

    report["model_validation"] = {
        "status": "OK" if scoring_ok else "FAIL",
        "model_loaded": True,
        "scoring_valid": scoring_ok,
        "sample_count": len(results),
    }
    return scoring_ok


