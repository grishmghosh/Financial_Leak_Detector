# STEP 2 — Dataset Validation Confirmation
# ═══════════════════════════════════════════════════════════════════
def step2_dataset_validation():
    print("\n" + "=" * 70)
    print("STEP 2 — Dataset Validation Confirmation")
    print("=" * 70)

    clean_path = Path("data/processed/payments_6500_rows_clean.csv")
    if not clean_path.exists():
        print("ERROR: Cleaned dataset not found!")
        report["dataset_validation"] = {"status": "FAIL", "reason": "file not found"}
        return None

    df = pd.read_csv(str(clean_path))

    # Column validation
    expected_cols = {"VOUCHER NUMBER", "AMOUNT", "CHECK DATE", "DEPARTMENT NAME", "VENDOR NAME"}
    actual_cols = set(df.columns)
    missing = expected_cols - actual_cols
    extra = actual_cols - expected_cols - {"YEAR", "MONTH"}

    print("\n--- Column Validation ---")
    print(f"  Expected:  {sorted(expected_cols)}")
    print(f"  Actual:    {sorted(df.columns.tolist())}")
    print(f"  Missing:   {sorted(missing) if missing else 'none'}")
    print(f"  Extra:     {sorted(extra) if extra else 'none (YEAR/MONTH are expected additions)'}")
    cols_ok = len(missing) == 0

    # Numeric amount parsing
    print("\n--- Amount Parsing ---")
    df["AMOUNT"] = pd.to_numeric(df["AMOUNT"], errors="coerce")
    null_amounts = df["AMOUNT"].isna().sum()
    print(f"  Dtype:       {df['AMOUNT'].dtype}")
    print(f"  Null values: {null_amounts}")
    amounts_ok = null_amounts == 0 and pd.api.types.is_numeric_dtype(df["AMOUNT"])

    # Datetime parsing
    print("\n--- Date Parsing ---")
    df["CHECK DATE"] = pd.to_datetime(df["CHECK DATE"], errors="coerce")
    null_dates = df["CHECK DATE"].isna().sum()
    print(f"  Dtype:       {df['CHECK DATE'].dtype}")
    print(f"  Null values: {null_dates}")
    dates_ok = null_dates == 0

    # Vendor name presence
    print("\n--- Vendor Name ---")
    null_vendors = df["VENDOR NAME"].isna().sum()
    print(f"  Null values: {null_vendors}")
    vendors_ok = null_vendors == 0

    # Voucher uniqueness
    print("\n--- Voucher Number Uniqueness ---")
    voucher_dupes = df["VOUCHER NUMBER"].duplicated().sum()
    print(f"  Duplicates:  {voucher_dupes}")
    vouchers_ok = voucher_dupes == 0

    # Dataset statistics
    print("\n--- Dataset Statistics ---")
    total = len(df)
    unique_vendors = df["VENDOR NAME"].nunique()
    unique_depts = df["DEPARTMENT NAME"].nunique()
    date_min = df["CHECK DATE"].min()
    date_max = df["CHECK DATE"].max()
    amt_min = df["AMOUNT"].min()
    amt_max = df["AMOUNT"].max()
    amt_mean = df["AMOUNT"].mean()
    amt_median = df["AMOUNT"].median()

    print(f"  Total rows:         {total:,}")
    print(f"  Unique vendors:     {unique_vendors}")
    print(f"  Unique departments: {unique_depts}")
    print(f"  Date range:         {date_min} to {date_max}")
    print(f"  Amount min:         ${amt_min:,.2f}")
    print(f"  Amount max:         ${amt_max:,.2f}")
    print(f"  Amount mean:        ${amt_mean:,.2f}")
    print(f"  Amount median:      ${amt_median:,.2f}")

    all_ok = cols_ok and amounts_ok and dates_ok and vendors_ok and vouchers_ok
    status = "VALID" if all_ok else "INVALID"
    print(f"\n  Dataset validation: {status}")

    report["dataset_validation"] = {
        "status": status,
        "total_rows": total,
        "unique_vendors": unique_vendors,
        "unique_departments": unique_depts,
        "date_range": f"{date_min} to {date_max}",
        "amount_min": float(amt_min),
        "amount_max": float(amt_max),
        "amount_mean": float(amt_mean),
        "amount_median": float(amt_median),
        "columns_ok": cols_ok,
        "amounts_ok": amounts_ok,
        "dates_ok": dates_ok,
        "vendors_ok": vendors_ok,
        "vouchers_unique": vouchers_ok,
    }
    return df


