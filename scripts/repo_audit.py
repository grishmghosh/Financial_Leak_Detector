# STEP 1 — Repository Integrity Audit
# ═══════════════════════════════════════════════════════════════════
def step1_repository_audit():
    print("\n" + "=" * 70)
    print("STEP 1 — Repository Integrity Audit")
    print("=" * 70)

    target_files = {
        "data/synthetic/payments_6500_rows.csv": "Raw synthetic dataset with 6500 rows and injected anomalies (duplicates, vendor anomalies, spikes). Generated by generate_synthetic_payments.py.",
        "data/processed/payments_6500_rows_clean.csv": "Cleaned synthetic dataset — dates parsed, numeric amounts, extra columns removed, YEAR/MONTH added. Produced by e2e_pipeline_validation.py Step 1.",
        "models/isolation_forest.pkl": "Trained Isolation Forest anomaly detection model. Trained in-memory by e2e_pipeline_validation.py on the 6500-row cleaned dataset.",
        "scripts/analyze_synthetic_dataset.py": "Standalone dataset analysis script — computes statistics, detects duplicates, vendor anomalies, keyword flags, department spikes.",
        "scripts/validate_pipeline.py": "Expected validation script (may not exist).",
    }

    file_audit = {}
    print(f"\n{'File':<55} {'Exists':<10} {'Size':<12}")
    print("-" * 77)

    for fpath, purpose in target_files.items():
        p = Path(fpath)
        exists = p.exists()
        size = f"{p.stat().st_size:,} B" if exists else "—"
        status = "YES" if exists else "NO"
        print(f"  {fpath:<53} {status:<10} {size:<12}")
        file_audit[fpath] = {"exists": exists, "purpose": purpose}

    # Check for duplicate / redundant scripts
    print("\n--- Duplicate / Redundant Script Analysis ---")

    scripts_dir = Path("scripts")
    all_scripts = sorted(scripts_dir.glob("*.py")) + sorted(scripts_dir.glob("*.sh"))
    print(f"\nAll files in scripts/:")
    for s in all_scripts:
        sz = s.stat().st_size
        print(f"  {s.name:<45} {sz:>8,} B")

    # Identify overlapping functionality
    overlaps = []

    e2e_val = Path("scripts/e2e_pipeline_validation.py")
    e2e_exec = Path("scripts/run_e2e_execution.py")
    if e2e_val.exists() and e2e_exec.exists():
        overlaps.append({
            "files": ["scripts/e2e_pipeline_validation.py", "scripts/run_e2e_execution.py"],
            "overlap": "Both perform end-to-end pipeline execution including dataset cleaning, ML scoring, and DB operations.",
            "recommendation": "KEEP BOTH — they serve different purposes. e2e_pipeline_validation.py is the offline-safe validator with DB fallback. run_e2e_execution.py is the full DB-dependent execution script. No conflict.",
        })

    analyze = Path("scripts/analyze_synthetic_dataset.py")
    if analyze.exists() and e2e_val.exists():
        overlaps.append({
            "files": ["scripts/analyze_synthetic_dataset.py", "scripts/e2e_pipeline_validation.py (Step 2)"],
            "overlap": "Both perform dataset analysis (duplicates, vendor anomalies, department spikes).",
            "recommendation": "KEEP BOTH — analyze_synthetic_dataset.py is a standalone analysis tool; e2e_pipeline_validation.py embeds analysis as part of the larger validation flow. No conflict.",
        })

    # Check for the two cleaned CSVs
    cleaned_1 = Path("data/processed/payments_cleaned.csv")
    cleaned_2 = Path("data/processed/payments_6500_rows_clean.csv")
    if cleaned_1.exists() and cleaned_2.exists():
        overlaps.append({
            "files": ["data/processed/payments_cleaned.csv", "data/processed/payments_6500_rows_clean.csv"],
            "overlap": "Two cleaned datasets in processed/. payments_cleaned.csv is from clean_data.py (raw data). payments_6500_rows_clean.csv is from e2e_pipeline_validation.py (synthetic data).",
            "recommendation": "KEEP BOTH — different source data. payments_cleaned.csv = cleaned raw historical. payments_6500_rows_clean.csv = cleaned synthetic for validation. Ensure ml_pipeline.py and the app code reference the correct one.",
        })

    if overlaps:
        for i, o in enumerate(overlaps, 1):
            print(f"\n  Overlap #{i}:")
            print(f"    Files:          {', '.join(o['files'])}")
            print(f"    Nature:         {o['overlap']}")
            print(f"    Recommendation: {o['recommendation']}")
    else:
        print("  No conflicting artifacts detected.")

    # validate_pipeline.py check
    vp = Path("scripts/validate_pipeline.py")
    if not vp.exists():
        print(f"\n  NOTE: scripts/validate_pipeline.py does NOT exist.")
        print(f"        This file was never created. Its intended role is covered")
        print(f"        by scripts/e2e_pipeline_validation.py.")

    report["repository_audit"] = {
        "files": file_audit,
        "overlaps": len(overlaps),
        "conflicts": 0,
        "validate_pipeline_exists": vp.exists(),
    }
    return file_audit


