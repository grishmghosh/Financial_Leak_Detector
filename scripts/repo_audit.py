# STEP 4 — Database Connectivity Check
# ═══════════════════════════════════════════════════════════════════
async def step4_db_check():
    print("\n" + "=" * 70)
    print("STEP 4 — Database Connectivity Check")
    print("=" * 70)

    try:
        from dotenv import load_dotenv
        load_dotenv()
    except ImportError:
        pass

    db_url = os.environ.get("DATABASE_URL", "")
    if not db_url:
        print("  DATABASE_URL not set — connection unavailable")
        report["database_status"] = "UNAVAILABLE (no DATABASE_URL)"
        return False

    # Mask the URL for printing
    if "@" in db_url:
        host_part = db_url.split("@")[1].split("/")[0]
        print(f"  Target host: {host_part}")
    print(f"  Attempting connection (5-second timeout)...")

    try:
        import asyncpg
        conn = await asyncio.wait_for(
            asyncpg.connect(dsn=db_url),
            timeout=5.0,
        )
        version = await conn.fetchval("SELECT version()")
        now = await conn.fetchval("SELECT now()")
        await conn.close()
        print(f"  DATABASE CONNECTION: SUCCESS")
        print(f"  Server version: {str(version)[:80]}")
        print(f"  Server time:    {now}")
        report["database_status"] = "SUCCESS"
        return True

    except asyncio.TimeoutError:
        print(f"  DATABASE CONNECTION: UNAVAILABLE (timeout after 5 seconds)")
        report["database_status"] = "UNAVAILABLE (timeout)"
        return False
    except Exception as e:
        print(f"  DATABASE CONNECTION: UNAVAILABLE ({type(e).__name__}: {e})")
        report["database_status"] = f"UNAVAILABLE ({type(e).__name__})"
        return False


